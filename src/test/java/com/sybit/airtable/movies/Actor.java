package com.sybit.airtable.movies;

import com.google.gson.annotations.SerializedName;

public class Actor {

    private String id;
    @SerializedName("Name")
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
